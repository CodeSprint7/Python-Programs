"""String methods part-1"""
s=input("Enter a string: ")
#converts string into lower case
print(s.lower())
#converts sting into upper case
print(s.upper())
#converts the first character into
#upper case
print(s.capitalize())
#also used to convert into lower case
print(s.casefold())
#lower case becomes upper & vice versa
print(s.swapcase())