"""String methods part-4"""
"""returns true if all the characters
in the string are capital letters"""
print("CODE SPRINT".isupper())
"""returns true if all the characters
in the string are small letters"""
print('code sprint'.islower())
"""returns true if the 1st letter of
every word is capital letter"""
print('Code Sprint'.istitle())
"""returns true if all the characters
in the string are whitespace"""
print('  '.isspace())